#!/usr/bin/env python3
"""
06_output_csv_join.py 📄
========================

Script 06 of 08 in the Networks Paper analysis pipeline.

PURPOSE:
--------
Combine all analysis outputs into a single unified dataset. This script merges
the results from network analysis, WUI classification, burn probability, and
RPS calculations into one comprehensive CSV file for statistical analysis.

WHY THIS MATTERS:
-----------------
Each previous script produces its own output CSV with different metrics.
To perform multivariate analysis (e.g., correlating egress capacity with
burn probability), we need all metrics in one dataset joined by GEOID.

WHAT IT JOINS:
--------------
1. Census attributes (population, demographics) from shapefiles
2. Network egress metrics (boundary crossings, lanes) from step 03
3. WUI land cover classification from step 04
4. Burn probability statistics from step 05
5. Risk to Potential Structures (RPS) from step 05

WORKFLOW:
---------
1. Load shapefile attributes for all CDPs
2. Load each analysis CSV (WUI, network, burn prob, RPS)
3. Normalize GEOID formats across all datasets
4. Merge all datasets on GEOID using left joins
5. Add state names from FIPS codes
6. Generate missing values report by state
7. Save combined CSV and missing values report

DEPENDENCIES (must run first):
------------------------------
- 01_download_designated_places.ipynb (Census shapefiles)
- 03_network_calc.py (network egress metrics)
- 04_wui_cdp_calculation.ipynb (WUI classification)
- 05_burn_prob_rps_calc.ipynb (burn probability and RPS)

================================================================================
📥 INPUTS (Required Data Sources)
================================================================================
1. WUI Land Cover CSV:
   - Path: {NETWORKS_DATA_DIR}/output_csvs/wui_land_cover_cdp/wui_land_cover_cdp.csv
   - Source: Generated by 04_wui_cdp_calculation.ipynb
   - Contains: WUI classification statistics per CDP

2. Network Egress Metrics CSV:
   - Path: {NETWORKS_DATA_DIR}/output_csvs/network_egress_cdp/network_egress_metrics_cdp.csv
   - Source: Generated by 03_network_calc.py
   - Contains: Road network egress statistics (boundary crossings, lanes, density)

3. Burn Probability CSV:
   - Path: {NETWORKS_DATA_DIR}/output_csvs/burn_prob_cdp/burn_prob_cdps_all_states.csv
   - Source: Generated by 05_burn_prob_rps_calc.ipynb
   - Contains: Burn probability zonal statistics (mean, min, max, etc.)

4. RPS (Risk to Potential Structures) CSV:
   - Path: {NETWORKS_DATA_DIR}/output_csvs/rps_cdp/rps_cdps_all_states.csv
   - Source: Generated by 05_burn_prob_rps_calc.ipynb
   - Contains: RPS zonal statistics per CDP

5. Census Place Shapefiles:
   - Path: {NETWORKS_DATA_DIR}/us-census-designated-places/
   - Source: Generated by 01_download_designated_places.ipynb
   - Contains: CDP boundaries and attributes

================================================================================
📤 OUTPUTS (Generated Files)
================================================================================
1. Combined CSV:
   - Path: {NETWORKS_DATA_DIR}/output_csvs/combined_csv/combined_data.csv
   - Contains: All CDP attributes merged on GEOID (demographics, WUI, network, burn, RPS)

2. Missing Values Report CSV:
   - Path: {NETWORKS_DATA_DIR}/output_csvs/combined_csv/missing_values_by_state.csv
   - Contains: Count of missing values per column, grouped by state

================================================================================
"""

import os
from pathlib import Path
