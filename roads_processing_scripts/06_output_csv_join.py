def load_shapefile_attributes(shp_root_dir: str) -> pd.DataFrame:
    """Load all place shapefiles and return attributes-only DataFrame.

    Returns an empty DataFrame if no shapefiles can be read.
    """
    shp_files = glob.glob(os.path.join(shp_root_dir, "*", "*.shp"))
    gdfs = []

    print("Loading shapefiles...")
    for shp in shp_files:
        try:
            gdf = gpd.read_file(shp)
            print(f"  Loaded shapefile: {shp} (polygons: {len(gdf)})")
            gdfs.append(gdf)
        except Exception as e:
            print(f"  Error reading {shp}: {e}")

    if not gdfs:
        print("No shapefiles were loaded; creating an empty DataFrame.")
        return pd.DataFrame()

    shp_concat = pd.concat(gdfs, ignore_index=True)
    print(f"\nTotal shapefile records after concatenation: {len(shp_concat)}")
    # Drop geometry to work with attributes only
    return shp_concat.drop(columns="geometry")


def read_csv_ensure_geoid(path: str) -> pd.DataFrame:
    """Read CSV and ensure GEOID column is string if present."""
    df = pd.read_csv(path)
    if "GEOID" in df.columns:
        df["GEOID"] = df["GEOID"].astype(str)
    return df


def determine_expected_geoid_length(shp_df: pd.DataFrame) -> int:
    """Infer expected GEOID length from shapefile attributes, default to 5."""
    if "GEOID" in shp_df.columns and not shp_df.empty:
        expected_length = shp_df["GEOID"].str.len().mode()[0]
        print(f"\nExpected GEOID length determined from shapefile: {expected_length}")
        return int(expected_length)
    print("\nShapefile GEOID not available; defaulting expected length to 5")
    return 5


def zfill_geoid(df: pd.DataFrame, length: int) -> pd.DataFrame:
    """Pad GEOID with leading zeros to a specific length if present."""
    if "GEOID" in df.columns:
        df["GEOID"] = df["GEOID"].astype(str).str.zfill(length)
    return df


################################################################################
#                               🚀 MAIN WORKFLOW                               #
################################################################################

def main():
    # -------------------------------
    # Step 1: Load shapefile attributes
    # -------------------------------
    shp_df = load_shapefile_attributes(shp_root)
    if "GEOID" in shp_df.columns:
        shp_df["GEOID"] = shp_df["GEOID"].astype(str)

    # -------------------------------
    # Step 2: Load CSVs
    # -------------------------------
    print("\nLoading CSV files...")
    wui_df = read_csv_ensure_geoid(wui_csv)
    print(f"  Loaded WUI CSV with {len(wui_df)} records.")
    network_df = read_csv_ensure_geoid(network_csv)
    print(f"  Loaded network CSV with {len(network_df)} records.")
    burn_df = read_csv_ensure_geoid(burn_csv)
    print(f"  Loaded burn probability CSV with {len(burn_df)} records.")
    rps_df = read_csv_ensure_geoid(rps_csv)
    print(f"  Loaded RPS CSV with {len(rps_df)} records.")

    # -------------------------------
    # Step 3: Normalize GEOID widths
    # -------------------------------
    expected_length = determine_expected_geoid_length(shp_df)
    shp_df = zfill_geoid(shp_df, expected_length)
    for df in [wui_df, network_df, burn_df, rps_df]:
        zfill_geoid(df, expected_length)

    # -------------------------------
    # Optional: dataset summaries
    # -------------------------------
    if VERBOSE_SUMMARIES:
        print("\n--- Individual Dataset Columns and Missing Values ---")
        print("\nWUI CSV Columns:")
        print(wui_df.columns.tolist())
        print("Missing values in WUI CSV:")
        print(wui_df.isnull().sum())

        print("\nNetwork CSV Columns:")
        print(network_df.columns.tolist())
        print("Missing values in Network CSV:")
        print(network_df.isnull().sum())

        print("\nBurn Probability CSV Columns:")
        print(burn_df.columns.tolist())
        print("Missing values in Burn Probability CSV:")
        print(burn_df.isnull().sum())

        print("\nRPS CSV Columns:")
        print(rps_df.columns.tolist())
        print("Missing values in RPS CSV:")
        print(rps_df.isnull().sum())

        if not shp_df.empty:
            print("\nShapefile Attributes Columns:")
            print(shp_df.columns.tolist())
            print("Missing values in Shapefile Attributes:")
            print(shp_df.isnull().sum())

    # -------------------------------
    # Step 4: Merge datasets on GEOID
    # -------------------------------
    if not shp_df.empty:
        combined_df = shp_df.merge(wui_df, on="GEOID", how="left")
    else:
        print("No shapefiles available. Using WUI CSV as base.")
        combined_df = wui_df.copy()

    combined_df = combined_df.merge(network_df, on="GEOID", how="left")
    combined_df = combined_df.merge(burn_df, on="GEOID", how="left")
    combined_df = combined_df.merge(rps_df, on="GEOID", how="left")

    print(f"\nCombined dataframe has {len(combined_df)} records and {len(combined_df.columns)} columns.")
    print("\nFinal Combined CSV Columns:")
    print(combined_df.columns.tolist())
    print("\nMissing values in Final Combined CSV:")
    print(combined_df.isnull().sum())

    # -------------------------------
    # Step 5: Add state name from FIPS and compute missing by state
    # -------------------------------
    # Using FIPS_TO_STATE mapping from config.py

    if 'State FIPS' in combined_df.columns:
        combined_df['State FIPS'] = combined_df['State FIPS'].astype(str).str.zfill(2)
    else:
        print("\nWarning: 'State FIPS' column not found in the combined dataframe.")

    combined_df['state_name'] = combined_df['State FIPS'].map(FIPS_TO_STATE)

    missing_data = []
    for col in combined_df.columns:
        if col == "state_name":
            continue
        # Group by state_name and count missing entries
        missing_counts = combined_df.groupby("state_name")[col].apply(lambda x: x.isnull().sum())
        for state, count in missing_counts.items():
            if count > 0:
                missing_data.append({'state_name': state, 'column': col, 'missing_count': count})

    missing_df = pd.DataFrame(missing_data)
    missing_df = missing_df.sort_values(["state_name", "column"]).reset_index(drop=True)
    print("\nMissing values by state and column:")
    print(missing_df)

    # -------------------------------
    # Step 6: Save outputs
    # -------------------------------
    combined_df.to_csv(output_csv, index=False)
    print(f"\nCombined CSV saved to: {output_csv}")

    missing_df.to_csv(missing_output_csv, index=False)
    print(f"Missing values DataFrame saved to: {missing_output_csv}")


if __name__ == "__main__":
    main()


