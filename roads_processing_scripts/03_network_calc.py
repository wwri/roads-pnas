def main():
    """Main execution function."""
    # Setup logging
    logging.basicConfig(
        filename=LOG_FILE,
        level=logging.INFO,
        format='%(asctime)s:%(levelname)s:%(message)s'
    )

    print_timestamped("🔍 Loading Census place data...")
    
    # Find all state directories
    all_state_dirs = [d for d in glob.glob(os.path.join(US_CENSUS_PLACES_DIR,'*')) if os.path.isdir(d)]
    if not all_state_dirs:
        logging.error("No state directories found.")
        print_timestamped("❌ Error: No state directories found.")
        return

    # Load and combine all place data
    gdf_all = gpd.GeoDataFrame()
    for sd in all_state_dirs:
        base = os.path.basename(sd)
        if '_' not in base:
            continue
        name, fp = base.rsplit('_',1)
        if SELECTED_STATES and name not in SELECTED_STATES:
            continue
        for shp in glob.glob(os.path.join(sd,'*.shp')):
            try:
                gdf = gpd.read_file(shp)
                gdf = gdf.set_crs(epsg=4326, allow_override=True) if gdf.crs is None else gdf.to_crs(epsg=4326)
                gdf['STATEFP'] = fp
                gdf['STATE_NAME'] = name
                gdf_all = pd.concat([gdf_all, gdf], ignore_index=True)
            except Exception as e:
                logging.warning(f"Error reading {shp}: {e}")

    if gdf_all.empty:
        logging.error("No places to process.")
        print_timestamped("❌ Error: No places to process.")
        return

    print_timestamped(f"📊 Processing {len(gdf_all)} places using {NUM_CORES} cores...")

    # Process places in parallel
    process_fn = partial(process_place, output_dir=OUTPUT_DIR, buffer_sizes_analysis=BUFFER_SIZES_ANALYSIS_METERS)
    all_stats = []
    all_errors = []
    
    with ProcessPoolExecutor(max_workers=NUM_CORES) as executor:
        futures = {executor.submit(process_fn, row): row for _, row in gdf_all.iterrows()}
        for future in tqdm(as_completed(futures), total=len(futures), desc="🔄 Processing places"):
            try:
                stats, errs = future.result()
                all_stats.extend(stats)
                all_errors.extend(errs)
            except Exception as e:
                logging.error(f"Error in future: {e}")

    # Save results
    print_timestamped("💾 Saving results to CSV...")
    pd.DataFrame(all_stats).to_csv(FINAL_CSV_OUTPUT, index=False)
    
    if all_errors:
        print_timestamped(f"⚠️  Completed with {len(all_errors)} errors; see {LOG_FILE} for details.")
    else:
        print_timestamped("✅ All places processed successfully!")
    
    print_timestamped(f"📈 Results saved to: {FINAL_CSV_OUTPUT}")

if __name__ == "__main__":
    main()
