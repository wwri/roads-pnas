"""
03_network_calc.py 📊
=====================

Script 03 of 08 in the Networks Paper analysis pipeline.

PURPOSE:
--------
Calculate road network egress metrics for each Census Designated Place (CDP).
This is the core analysis script that quantifies how many roads (and lanes) 
cross the boundary of each community, which is critical for understanding 
evacuation capacity during wildfires.

WHAT IT CALCULATES:
-------------------
For each CDP, this script computes:
- Graph density: How interconnected the road network is
- Average node degree: Average number of roads meeting at intersections
- Boundary crossing counts: Number of roads crossing the CDP boundary by type
  (motorway, trunk, primary, secondary, tertiary)
- Lane counts: Total lanes for each road type crossing the boundary

WHY THIS MATTERS:
-----------------
Communities with few egress routes face bottlenecks during evacuations.
By counting boundary-crossing roads, we can identify which communities
have limited evacuation infrastructure and may need additional planning.

WORKFLOW:
---------
1. Load all CDP shapefiles from each state
2. For each CDP:
   a. Load the pre-computed road network graph (GraphML)
   b. Buffer the CDP boundary by specified distance (default: 500m)
   c. Clip the road network to the buffered boundary
   d. Calculate network statistics (density, degree, etc.)
   e. Count roads/lanes crossing the boundary by highway type
   f. Generate visualization plot (optional)
   g. Save boundary-crossing roads to CSV
3. Combine all results into a single output CSV
4. Process places in parallel for efficiency

DEPENDENCIES (must run first):
------------------------------
- 01_download_designated_places.ipynb (Census shapefiles)
- 02_osmnx_network_download.ipynb (GraphML road networks)

================================================================================
📥 INPUTS (Required Data Sources)
================================================================================
1. Census Place Shapefiles:
   - Location: {NETWORKS_DATA_DIR}/us-census-designated-places/
   - Format: Shapefiles (.shp) organized by state (e.g., California_06/tl_2023_06_place.shp)
   - Contains: Place boundaries, GEOID, NAME, STATE_NAME, population data
   - Source: Generated by 01_download_designated_places.ipynb

2. Road Network Graphs (GraphML):
   - Location: {NETWORKS_DATA_DIR}/output_maps/{State}_{FIPS}/{Place}_{GEOID}/
   - Format: GraphML files (e.g., Paradise_0655528.graphml)
   - Contains: OSM road network data for each place
   - Source: Generated by 02_osmnx_network_download.ipynb

================================================================================
📤 OUTPUTS (Generated Files)
================================================================================
1. Network Egress Metrics CSV:
   - Path: {NETWORKS_DATA_DIR}/output_csvs/network_egress_cdp/network_egress_metrics_cdp.csv
   - Contains: Network statistics per CDP (density, degree, boundary crossings by road type)

2. Visualization Maps (PNG):
   - Path: {NETWORKS_DATA_DIR}/output_maps/{State}_{FIPS}/{Place}_{GEOID}/
   - Format: {Place}_{GEOID}_visualization_{buffer}m.png
   - Contains: Road network maps with color-coded highway types and boundary crossing stats

3. Boundary Crossing Roads CSV (per place):
   - Path: {Place_Dir}/csv/boundary_crossing_roads_{buffer}m.csv
   - Contains: Individual road segments that cross the buffered boundary

4. Log File:
   - Path: <script_directory>/processing.log
   - Contains: Processing status, errors, and timing information

================================================================================
"""

