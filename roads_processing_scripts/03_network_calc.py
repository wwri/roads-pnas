# This is the main workhorse function that processes each CDP individually.
# It runs in parallel across multiple cores for efficiency.

def process_place(place, output_dir, buffer_sizes_analysis):
    """
    Process a single Census place to compute network egress statistics.
    
    This function performs the complete analysis for one CDP:
    1. Loads the pre-computed road network graph
    2. Creates a buffered boundary around the CDP
    3. Clips the network to the buffered area
    4. Calculates network topology statistics
    5. Counts roads crossing the boundary by type
    6. Generates a visualization plot (if enabled)
    7. Saves results to CSV files
    
    The boundary crossing analysis is the key output - it tells us how many
    roads (and lanes) connect the community to the outside world, which
    directly relates to evacuation capacity.
    
    Args:
        place: GeoDataFrame row containing:
            - NAME: Place name (e.g., "Paradise")
            - GEOID: Geographic identifier (e.g., "0655528")
            - STATE_NAME: State name (e.g., "California")
            - STATEFP: State FIPS code (e.g., "06")
            - geometry: Place boundary polygon
        output_dir: Directory to save outputs (maps, CSVs)
        buffer_sizes_analysis: List of buffer distances in meters (e.g., [500])
        
    Returns:
        tuple: (stats_list, error_messages)
            - stats_list: List of dicts with network statistics per buffer size
            - error_messages: List of any error strings encountered
    """
    signal.signal(signal.SIGALRM, timeout_handler)
    signal.alarm(TIMEOUT_SECONDS)

    # Relevant highway types for analysis
    relevant_highway_types = ['motorway', 'trunk', 'primary', 'secondary', 'tertiary']
    
    # Color mapping for different highway types
    color_map = ROAD_COLOR_PALETTE.copy()

    def is_relevant_road(hw):
        """Check if highway type is relevant for analysis."""
        hw_list = extract_hw_types(hw)
        return any(sanitize_hw_type(t) in relevant_highway_types for t in hw_list)

    stats_list = []
    error_messages = []

    try:
        place_name = place['NAME']
        geoid      = place['GEOID']
        state_name = place['STATE_NAME']
        statefp    = place['STATEFP']
        polygon    = place.geometry

        # Prepare output directories
        state_dir_name = f"{state_name}_{statefp}"
        sanitized_name = "".join(c if c.isalnum() else "_" for c in place_name).strip("_")
        place_folder   = f"{sanitized_name}_{geoid}"
        place_dir      = os.path.join(output_dir, state_dir_name, place_folder)
        os.makedirs(place_dir, exist_ok=True)
        csv_dir = os.path.join(place_dir, 'csv')
        os.makedirs(csv_dir, exist_ok=True)

        # Load saved GraphML
        graph_path = os.path.join(place_dir, f"{sanitized_name}_{geoid}.graphml")
        if not os.path.exists(graph_path):
            error_messages.append(f"GraphML not found for {place_name} ({geoid}).")
            return stats_list, error_messages
        G = ox.load_graphml(graph_path)

        # Project polygon to UTM
        poly_ser = gpd.GeoSeries([polygon], crs='EPSG:4326')
        utm_crs  = poly_ser.estimate_utm_crs()
        poly_utm = poly_ser.to_crs(utm_crs)

        # Extract nodes and edges
        nodes_full, edges_full = ox.graph_to_gdfs(G)
        if edges_full is None or edges_full.empty:
            # No edges → empty stats & CSVs
            for buf in buffer_sizes_analysis:
                stats_list.append({
                    'GEOID': geoid, 'buffer_distance': buf,
                    'graph_density': np.nan,
                    'average_degree': np.nan,
                    'num_nodes': 0
                })
                pd.DataFrame(columns=['highway','geometry']).to_csv(
                    os.path.join(csv_dir, f"boundary_crossing_roads_{buf}m.csv"),
                    index=False
                )
            return stats_list, error_messages

        # Ensure edges are in EPSG:4326
        if edges_full.crs is None:
            edges_full.set_crs(epsg=4326, inplace=True)
        elif edges_full.crs.to_string() != 'EPSG:4326':
            edges_full = edges_full.to_crs(epsg=4326)

        base_data = {'GEOID': geoid}

        # Process each buffer size
        for buf in buffer_sizes_analysis:
            stats = base_data.copy()
            stats['buffer_distance'] = buf

            buf_poly_utm = poly_utm.buffer(buf)
            buf_polygon   = buf_poly_utm.to_crs('EPSG:4326').iloc[0]

            # Truncate graph to buffered polygon
            try:
                G_clip = ox.truncate.truncate_graph_polygon(
                    G, buf_polygon, retain_all=True, truncate_by_edge=True
                )
            except ValueError:
                stats.update({'graph_density':np.nan,'average_degree':np.nan,'num_nodes':0})
                stats_list.append(stats)
                pd.DataFrame(columns=edges_full.columns).to_csv(
                    os.path.join(csv_dir, f"boundary_crossing_roads_{buf}m.csv"),
                    index=False
                )
                continue

            if G_clip.number_of_nodes() == 0:
                stats.update({'graph_density':np.nan,'average_degree':np.nan,'num_nodes':0})
                stats_list.append(stats)
                pd.DataFrame(columns=edges_full.columns).to_csv(
                    os.path.join(csv_dir, f"boundary_crossing_roads_{buf}m.csv"),
                    index=False
                )
                continue

            # Compute basic network statistics
            try:
                basic = ox.basic_stats(G_clip, area=buf_polygon.area)
                stats.update(basic)
            except Exception:
                stats.update({'density':np.nan,'avg_clustering':np.nan})
            
            stats['graph_density']   = nx.density(G_clip)
            degrees = [d for _, d in G_clip.degree()]
            stats['average_degree']  = np.mean(degrees) if degrees else 0
            stats['num_nodes']       = len(G_clip.nodes)

            # Initialize boundary crossing counters
            for hw in relevant_highway_types:
                stats[f'boundary_crossing_edges_{hw}'] = 0
                stats[f'boundary_crossing_lanes_{hw}'] = 0

            # Find edges crossing boundary
            boundary_edges = edges_full[edges_full.geometry.crosses(get_exterior_boundary(buf_polygon))]
            if 'highway' in boundary_edges.columns:
                relevant = boundary_edges[boundary_edges['highway'].apply(is_relevant_road)]
            else:
                relevant = gpd.GeoDataFrame(columns=edges_full.columns)

            # Save boundary crossing roads to CSV
            relevant.to_csv(
                os.path.join(csv_dir, f"boundary_crossing_roads_{buf}m.csv"),
                index=False
            )

            # Tally edges & lanes by highway type
            for _, row in relevant.iterrows():
                hw_vals = extract_hw_types(row.get('highway'))
                lanes   = row.get('lanes', 1)
                try:
                    lane_count = int(lanes) if not pd.isna(lanes) else 1
                except:
                    lane_count = 1
                for t in hw_vals:
                    t_s = sanitize_hw_type(t)
                    if t_s in relevant_highway_types:
                        stats[f'boundary_crossing_edges_{t_s}'] += 1
                        stats[f'boundary_crossing_lanes_{t_s}'] += lane_count

            # =============================================================================
            # 📊 PLOTTING SECTION - Create visualization plots
            # =============================================================================
            if CREATE_PLOTS:
                fig, ax = plt.subplots(figsize=(10,10), dpi=PLOT_DPI)

                # Plot original CDP polygon (light blue fill)
                gpd.GeoSeries([polygon], crs='EPSG:4326').plot(
                    ax=ax, facecolor='#ccf2ff', edgecolor='none', alpha=0.5
                )

                # Plot buffered boundary
                gpd.GeoSeries([buf_polygon]).plot(
                    ax=ax,
                    facecolor='none', edgecolor='black', lw=2
                )

                # Get just the clipped edges for plotting - draw each road type in its own color
                clipped = edges_full[edges_full.index.isin(G_clip.edges())]
                if not clipped.empty:
                    # Draw residential FIRST so it sits under all other road classes
                    overlay_order = ['residential', 'tertiary', 'secondary', 'primary', 'trunk', 'motorway']
                    # Track which edges get classified so unclassified ones can be drawn in gray
                    classified_idx = set()
                    for hw_type in overlay_order:
                        col = color_map.get(hw_type)
                        if col is None:
                            continue
                        mask = clipped['highway'].apply(
                            lambda h: hw_type in extract_hw_types(h)
                        ) if 'highway' in clipped.columns else pd.Series(False, index=clipped.index)
                        subset = clipped[mask]
                        if not subset.empty:
                            classified_idx.update(subset.index.tolist())
                            lw = 2 if hw_type in ['motorway', 'trunk'] else 1.5
                            z = 1 if hw_type == 'residential' else 2
                            subset.plot(ax=ax, linewidth=lw, color=col, zorder=z)
                    # Draw any unclassified edges (e.g. service roads, unclassified) in gray
                    unclassified = clipped[~clipped.index.isin(classified_idx)]
                    if not unclassified.empty:
                        unclassified.plot(ax=ax, linewidth=1, color='gray', zorder=0)

                # Remove ticks & labels for cleaner appearance
                ax.set_xticks([])
                ax.set_yticks([])

                # Create legend with road types and boundaries
                legend_handles = [
                    Patch(facecolor='#ccf2ff', edgecolor='none', label='Original CDP'),
                    Line2D([0],[0], color='black', lw=2, label='Buffered CDP boundary'),
                    Line2D([0],[0], color='gray',  lw=2, label='Other roads'),
                    Line2D([0],[0], color=color_map['motorway'],  lw=4, label='Motorway'),
                    Line2D([0],[0], color=color_map['trunk'],     lw=4, label='Trunk'),
                    Line2D([0],[0], color=color_map['primary'],   lw=4, label='Primary'),
                    Line2D([0],[0], color=color_map['secondary'], lw=4, label='Secondary'),
                    Line2D([0],[0], color=color_map['tertiary'],  lw=4, label='Tertiary'),
                    Line2D([0],[0], color=color_map['residential'], lw=1.5, label='Residential'),
                ]
                ax.legend(handles=legend_handles, bbox_to_anchor=(1.05,1), loc='upper left')

                # Add scale bar (placed OUTSIDE the plot area, in the bottom figure margin)
                scale_len = 5 / 111.32  # 5 km in degrees (1 degree latitude ~ 111.32 km)
                sb = AnchoredSizeBar(
                    ax.transData, scale_len, "",
                    loc='lower center', pad=0.1, borderpad=0.5,
                    color='black', frameon=False, size_vertical=0.001,
                    fontproperties=fm.FontProperties(size=10),
                    bbox_to_anchor=(0.5, 0.075), bbox_transform=fig.transFigure
                )
                ax.add_artist(sb)

                # Add statistics text and title
                # Increase bottom margin to make room for outside scalebar and stats text
                plt.tight_layout(rect=[0,0.18,0.85,1])
                stats_txt = (
                    f"Boundary Crossing Roads:\n"
                    f"Motorway: {stats.get('boundary_crossing_edges_motorway',0)} roads, "
                    f"{stats.get('boundary_crossing_lanes_motorway',0)} lanes\n"
                    f"Trunk: {stats.get('boundary_crossing_edges_trunk',0)} roads, "
                    f"{stats.get('boundary_crossing_lanes_trunk',0)} lanes\n"
                    f"Primary: {stats.get('boundary_crossing_edges_primary',0)} roads, "
                    f"{stats.get('boundary_crossing_lanes_primary',0)} lanes\n"
                    f"Secondary: {stats.get('boundary_crossing_edges_secondary',0)} roads, "
                    f"{stats.get('boundary_crossing_lanes_secondary',0)} lanes\n"
                    f"Tertiary: {stats.get('boundary_crossing_edges_tertiary',0)} roads, "
                    f"{stats.get('boundary_crossing_lanes_tertiary',0)} lanes"
                )
                # Move stats box to the bottom-left so it doesn't cover the centered scale bar
                fig.text(
                    0.08, 0.02, stats_txt,
                    ha='left', va='bottom',
                    fontsize=12,
                    bbox=dict(facecolor='white', alpha=0.7)
                )
                fig.text(
                    0.5, 0.97, f"{state_name} - {place_name}",
                    ha='center', va='top',
                    fontsize=14, fontweight='bold',
                    bbox=dict(facecolor='white', alpha=0.7)
                )

                # Save the plot
                plt.savefig(
                    os.path.join(
                        place_dir,
                        f"{sanitized_name}_{geoid}_visualization_{buf}m.png"
                    ),
                    dpi=PLOT_DPI
                )
                plt.close(fig)

            stats_list.append(stats)

    except TimeoutError:
        logging.error(f"Timeout after {TIMEOUT_SECONDS}s for {place.get('NAME','Unknown')} ({place.get('GEOID','Unknown')})")
    except Exception as e:
        logging.error(f"Unexpected error for {place.get('NAME')}: {e}\n{traceback.format_exc()}")
    finally:
        signal.alarm(0)

    return stats_list, error_messages

