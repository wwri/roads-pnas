def sanitize_hw_type(hw_type):
    """
    Convert highway type attribute to a clean string for consistent matching.
    
    OSM highway attributes can be strings, lists, or nested structures.
    This function normalizes them to simple strings for comparison.
    
    Examples:
        "primary" -> "primary"
        ["primary", "secondary"] -> "primary_secondary"
        "['motorway']" -> "motorway"
    
    Args:
        hw_type: Highway type (string, list, or other format from OSM)
        
    Returns:
        str: Sanitized highway type string with spaces/brackets removed
    """
    hw_str = str(hw_type)
    return hw_str.replace(' ', '_') \
                 .replace('[', '') \
                 .replace(']', '') \
                 .replace(',', '') \
                 .replace("'", "")

def extract_hw_types(hw):
    """
    Given a highway attribute (string, list, nested list, etc.), return
    a flat list of individual highway-type strings.
    
    Args:
        hw: Highway type attribute (various formats)
        
    Returns:
        list: Flat list of highway type strings
    """
    types = []
    if isinstance(hw, str):
        s = hw.strip()
        if s.startswith('[') and s.endswith(']'):
            try:
                parsed = ast.literal_eval(s)
                return extract_hw_types(parsed)
            except Exception:
                types.append(s)
        else:
            types.append(s)
    elif isinstance(hw, (list, tuple)):
        for item in hw:
            types.extend(extract_hw_types(item))
    else:
        types.append(str(hw))
    return types

def get_exterior_boundary(polygon):
    """
    Extract exterior boundary from polygon geometry.
    
    Args:
        polygon: Shapely polygon or multipolygon
        
    Returns:
        Exterior boundary geometry
    """
    if isinstance(polygon, Polygon):
        return polygon.exterior
    elif isinstance(polygon, MultiPolygon):
        return MultiLineString([p.exterior for p in polygon.geoms])
    else:
        raise ValueError("Unsupported geometry type for boundary extraction.")

def timeout_handler(signum, frame):
    """Handle timeout signals during processing."""
    raise TimeoutError("Operation timed out inside worker process.")

